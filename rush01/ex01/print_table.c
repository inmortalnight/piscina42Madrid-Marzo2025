#include <unistd.h>

void	print_table(int **table)
{
	int	i; 
	int	j;
	char	print;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			print = table[i][j] + '0';
			write(1, &print, 1);
			write(1, " ", 1);
			j++;
		}
		i++;
		write(1, "\n", 1);
	}
}
